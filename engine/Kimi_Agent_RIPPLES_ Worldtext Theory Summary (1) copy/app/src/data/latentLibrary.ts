// Helper function to get random description from array
export function getRandomDescription(descriptions: string[]): string {
  return descriptions[Math.floor(Math.random() * descriptions.length)];
}

// Helper function to get description for specific entity and vector
export function getEntityDescription(
  scenarioId: string, 
  entityId: string, 
  vector: string
): string {
  const scenario = latentLibrary.scenarios[scenarioId];
  if (!scenario) return '';
  
  const entity = scenario.entities[entityId];
  if (!entity) return '';
  
  const descriptions = entity[vector as keyof typeof entity];
  if (!descriptions || descriptions.length === 0) return '';
  
  return getRandomDescription(descriptions);
}

// Helper function to get baseline descriptions
export function getBaselineDescriptions(scenarioId: string): string[] {
  const scenario = latentLibrary.scenarios[scenarioId];
  if (!scenario) return [];
  return scenario.baseline;
}

// Helper function to get environmental descriptions
export function getEnvironmentalDescriptions(scenarioId: string, factor: string): string {
  const scenario = latentLibrary.scenarios[scenarioId];
  if (!scenario) return '';
  
  const descriptions = scenario.environmental[factor as keyof typeof scenario.environmental];
  if (!descriptions || descriptions.length === 0) return '';
  
  return getRandomDescription(descriptions);
}
